#!/usr/bin/env bash
# Register a site in platform/registry/sites.yaml and scaffold prod dirs.
# Called from CRM provisioning or manually.
#
# Usage:
#   site-register.sh <domain> [--registry-only]
#
# Env:
#   SITE_REPO, SITE_API_PORT, SITE_WEB_PORT, SITE_EXTRA_DOMAINS (comma-separated)
#   PLATFORM_REGISTRY — path to sites.yaml (default: repo platform/registry/sites.yaml)
set -euo pipefail

DOMAIN="${1:?domain}"
MODE="${2:-}"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
REGISTRY="${PLATFORM_REGISTRY:-$REPO_ROOT/platform/registry/sites.yaml}"

API_PORT="${SITE_API_PORT:-8080}"
WEB_PORT="${SITE_WEB_PORT:-8088}"
REPO_URL="${SITE_REPO:-}"
EXTRA_DOMAINS="${SITE_EXTRA_DOMAINS:-}"

python3 - "$REGISTRY" "$DOMAIN" "$API_PORT" "$WEB_PORT" "$REPO_URL" "$EXTRA_DOMAINS" <<'PY'
import json
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    sys.stderr.write("Install PyYAML: apt install python3-yaml\n")
    sys.exit(1)

registry_path, domain, api_port, web_port, repo_url, extra_domains = sys.argv[1:7]
path = Path(registry_path)
data = yaml.safe_load(path.read_text()) if path.exists() else {"sites": {}}
sites = data.setdefault("sites", {})

slug = domain.strip().lower()
extra = [d.strip().lower() for d in extra_domains.split(",") if d.strip()]
domains = [slug, f"www.{slug}", *extra]
domains = list(dict.fromkeys(domains))

schema_prefix = slug.replace(".", "_").replace("-", "_") + "_"

if slug not in sites:
    sites[slug] = {
        "slug": slug,
        "repo": repo_url or f"git@github.com:org/{slug.replace('.', '-')}.git",
        "prod": {
            "path": f"/srv/sites/{slug}",
            "api_port": int(api_port),
            "web_port": int(web_port),
            "domains": domains,
        },
        "dev": {
            "shared_infra": True,
            "postgres_host_port": 5438,
            "schema_prefix": schema_prefix,
            "vite_port_base": 5170,
            "api_port_base": 8090,
            "domain_pattern": "{user}." + slug,
            "users": {},
        },
    }
else:
    entry = sites[slug]
    entry.setdefault("prod", {})
    entry["prod"]["api_port"] = int(api_port)
    entry["prod"]["web_port"] = int(web_port)
    if repo_url:
        entry["repo"] = repo_url
    prod_domains = entry["prod"].setdefault("domains", domains)
    for d in domains:
        if d not in prod_domains:
            prod_domains.append(d)

path.parent.mkdir(parents=True, exist_ok=True)
path.write_text(yaml.safe_dump(data, sort_keys=False, allow_unicode=True))
print(f"Updated registry: {path}")
PY

WORKSPACES_DIR="$REPO_ROOT/workspaces"
mkdir -p "$WORKSPACES_DIR"
ENV_TEMPLATE="$WORKSPACES_DIR/web.env.${DOMAIN//./_}.example"
if [[ ! -f "$ENV_TEMPLATE" ]]; then
  cat > "$ENV_TEMPLATE" <<EOF
# Web env template for site $DOMAIN — generated by site-register.sh
VITE_DEV_PORT=5170
VITE_DEV_API_PORT=8090
VITE_PUBLIC_SITE_URL=https://USER.$DOMAIN
EOF
  echo "Created $ENV_TEMPLATE"
fi

if [[ "$MODE" == "--registry-only" ]]; then
  exit 0
fi

SITE_ADD="${SCRIPT_DIR}/site-add.sh"
if [[ -x "$SITE_ADD" ]] || [[ -f "$SITE_ADD" ]]; then
  if [[ "$(id -u)" -eq 0 ]]; then
    bash "$SITE_ADD" "$DOMAIN" "$REPO_URL"
  else
    echo "Skip site-add.sh (sudo required for /srv/sites scaffold)"
  fi
fi

echo "Site $DOMAIN registered"
